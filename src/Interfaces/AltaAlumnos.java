    private void botonAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAltaActionPerformed
        DataBase d = new DataBase();
        PreparedStatement ps=null;
        try{
        d.Conectar("localhost", "3306", "alumnos", "usuario", "usuario");
        ps = d.prepararSentencia("INSERT INTO alumno VALUES(?,?,?,?,?,?,?);");
        ps.setString(1, tExpediente.getText());
        ps.setString(2, tNombre.getText());
        ps.setString(3, tTelefono.getText());
        ps.setString(4, tDireccion.getText());
        ps.setInt(5, Integer.parseInt(tEdad.getText()));
        ps.setInt(6,0);
        ps.setInt(7,0);
        
        int resultado = ps.executeUpdate();
        }catch(SQLException e){
            System.out.println(e.getMessage());
        }
        finally {          
           // Cerrar statement
           if(ps != null) {
               try {
                   ps.close();
               } catch (SQLException ex) {
                   ex.getMessage();
               }
           }
           d.Desconectar();
        }
    }//GEN-LAST:event_botonAltaActionPerformed

