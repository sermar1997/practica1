    private void botonNotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonNotaActionPerformed
        DataBase d = new DataBase();
        ResultSet rs = null;
        PreparedStatement ps = null;
        try {
            d.Conectar("localhost", "3306", "alumnos", "usuario", "usuario");
            ps = d.prepararSentencia("SELECT * FROM ALUMNO WHERE EXPEDIENTE LIKE ?;");
            ps.setString(1, tExpediente.getText());
            rs = ps.executeQuery();
            if (rs.next()) {
                tEntornos.setText(String.valueOf(rs.getInt(6)));
                tProg.setText(String.valueOf(rs.getInt(7)));
            } else {
                JOptionPane.showMessageDialog(this, "Este Alumno no existe en la base de datos");
            }

        } catch (SQLException e) {
            e.printStackTrace();
        } finally {
            // Cerrar statement
            if (rs != null) {
                try {
                    rs.close();
                } catch (SQLException ex) {
                    ex.getMessage();
                }
            }
            d.Desconectar();
        }
    }//GEN-LAST:event_botonNotaActionPerformed

